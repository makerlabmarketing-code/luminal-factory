create table if not exists public.employee_permissions (
  id bigint generated by default as identity primary key,
  employee_id bigint not null,
  permission_code text not null,
  effect text not null,
  status text not null default 'ACTIVE',
  granted_at timestamp with time zone not null default now(),
  granted_by_employee_id bigint null,
  revoked_at timestamp with time zone null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint employee_permissions_employee_id_fkey
    foreign key (employee_id)
    references public.employees(id)
    on update cascade
    on delete restrict,

  constraint employee_permissions_permission_code_fkey
    foreign key (permission_code)
    references public.permissions(code)
    on update cascade
    on delete restrict,

  constraint employee_permissions_granted_by_employee_id_fkey
    foreign key (granted_by_employee_id)
    references public.employees(id)
    on update cascade
    on delete set null,

  constraint employee_permissions_effect_check
    check (effect in ('ALLOW', 'DENY')),

  constraint employee_permissions_status_check
    check (status in ('ACTIVE', 'INACTIVE')),

  constraint employee_permissions_revoked_check
    check (
      (status = 'ACTIVE' and revoked_at is null)
      or status = 'INACTIVE'
    )
);

create unique index if not exists employee_permissions_one_active_effect
on public.employee_permissions (employee_id, permission_code, effect)
where status = 'ACTIVE';

create index if not exists employee_permissions_employee_id_idx
on public.employee_permissions (employee_id);

create index if not exists employee_permissions_permission_status_idx
on public.employee_permissions (permission_code, status);

