create or replace function public.current_employee_id()
returns bigint
language sql
stable
security definer
set search_path = public, auth, pg_temp
as $$
  select e.id
  from public.employees e
  where e.auth_user_id = (select auth.uid())
    and e.status = 'ACTIVE'
    and coalesce(e.is_active, true) = true
  limit 1;
$$;

create or replace function public.has_workspace_access(workspace_code text)
returns boolean
language sql
stable
security definer
set search_path = public, auth, pg_temp
as $$
  select
    workspace_code in ('STAFF_WORKSPACE', 'ADMIN_WORKSPACE')
    and public.current_employee_id() is not null
    and exists (
      select 1
      from public.employee_workspace_access access
      where access.employee_id = public.current_employee_id()
        and access.workspace = workspace_code
        and access.status = 'ACTIVE'
        and access.revoked_at is null
    );
$$;

create or replace function public.has_permission(permission_code text)
returns boolean
language sql
stable
security definer
set search_path = public, auth, pg_temp
as $$
  select
    public.current_employee_id() is not null
    and exists (
      select 1
      from public.permissions p
      where p.code = permission_code
    )
    and not exists (
      select 1
      from public.employee_permissions ep
      where ep.employee_id = public.current_employee_id()
        and ep.permission_code = permission_code
        and ep.effect = 'DENY'
        and ep.status = 'ACTIVE'
        and ep.revoked_at is null
    )
    and exists (
      select 1
      from public.employee_permissions ep
      where ep.employee_id = public.current_employee_id()
        and ep.permission_code = permission_code
        and ep.effect = 'ALLOW'
        and ep.status = 'ACTIVE'
        and ep.revoked_at is null
    );
$$;

create or replace function public.can_access_admin()
returns boolean
language sql
stable
security definer
set search_path = public, auth, pg_temp
as $$
  select public.has_workspace_access('ADMIN_WORKSPACE');
$$;

create or replace function public.can_access_staff()
returns boolean
language sql
stable
security definer
set search_path = public, auth, pg_temp
as $$
  select public.has_workspace_access('STAFF_WORKSPACE');
$$;

