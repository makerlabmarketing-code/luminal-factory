-- Access & Permission Foundation Slice 1.
--
-- Scope:
-- - Create workspace access and employee permission override tables.
-- - Seed the stable permission catalog.
-- - Create read-only authorization helpers.
-- - Enable RLS on new foundation tables.
-- - Add minimal read policies only.
--
-- This migration intentionally does not:
-- - Backfill any employee access or permissions.
-- - Replace public.is_app_admin().
-- - Modify existing RLS policies.
-- - Modify admin/staff routes, middleware, menus, employees.role, or project_members.
-- - Create Auth users or send invites.

begin;

create table if not exists public.employee_workspace_access (
  id bigint generated by default as identity primary key,
  employee_id bigint not null,
  workspace text not null,
  status text not null default 'ACTIVE',
  granted_at timestamp with time zone not null default now(),
  granted_by_employee_id bigint null,
  revoked_at timestamp with time zone null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),

  constraint employee_workspace_access_employee_id_fkey
    foreign key (employee_id)
    references public.employees(id)
    on update cascade
    on delete restrict,

  constraint employee_workspace_access_granted_by_employee_id_fkey
    foreign key (granted_by_employee_id)
    references public.employees(id)
    on update cascade
    on delete set null,

  constraint employee_workspace_access_workspace_check
    check (workspace in ('STAFF_WORKSPACE', 'ADMIN_WORKSPACE')),

  constraint employee_workspace_access_status_check
    check (status in ('ACTIVE', 'INACTIVE')),

  constraint employee_workspace_access_revoked_check
    check (
      (status = 'ACTIVE' and revoked_at is null)
      or status = 'INACTIVE'
    )
);

create unique index if not exists employee_workspace_access_one_active
on public.employee_workspace_access (employee_id, workspace)
where status = 'ACTIVE';

create index if not exists employee_workspace_access_employee_id_idx
on public.employee_workspace_access (employee_id);

create index if not exists employee_workspace_access_workspace_status_idx
on public.employee_workspace_access (workspace, status);

