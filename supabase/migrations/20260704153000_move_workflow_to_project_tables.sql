-- Purpose:
-- Move production workflow data out of system_settings and into dedicated
-- projects, phases, and tasks tables. system_settings remains for application
-- configuration such as automatic email reminder settings.
--
-- Impact:
-- Existing records in system_settings where group_name = 'PRODUCTION_WORKFLOW'
-- are copied into the workflow tables. Existing workflow tables are preserved
-- and only missing columns are added.
--
-- Rollback:
-- Keep system_settings records until the new UI has been verified. To roll
-- back app code, switch readers/writers back to system_settings. To remove
-- the new columns/tables after rollback, drop tasks, phases, projects only if
-- they are not used by other screens.

create table if not exists public.projects (
  id bigint generated by default as identity primary key,
  name text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.phases (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.projects(id) on delete cascade,
  name text not null,
  order_index integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.tasks (
  id bigint generated by default as identity primary key,
  phase_id bigint not null references public.phases(id) on delete cascade,
  name text not null,
  assignee text,
  deadline text,
  note text,
  status text not null default 'TODO',
  created_at timestamptz not null default now()
);

