create index if not exists phases_project_id_order_index_idx
  on public.phases(project_id, order_index);

create index if not exists tasks_phase_id_idx
  on public.tasks(phase_id);

with workflow_settings as (
  select
    ss.key,
    ss.value as phase_status,
    split_part(ss.config_name, ' - ', 1) as project_name,
    nullif(substring(ss.config_name from position(' - ' in ss.config_name) + 3), '') as phase_name,
    coalesce(ss.description::jsonb, '{}'::jsonb) as payload,
    row_number() over (
      partition by split_part(ss.config_name, ' - ', 1)
      order by ss.key
    ) - 1 as phase_order
  from public.system_settings ss
  where ss.group_name = 'PRODUCTION_WORKFLOW'
    and ss.config_name is not null
),
inserted_projects as (
  insert into public.projects (name, project_deadline, drive_link)
  select
    project_name,
    max(payload->>'project_deadline'),
    max(payload->>'project_drive_link')
  from workflow_settings
  where project_name <> ''
    and not exists (
      select 1
      from public.projects p
      where p.name = workflow_settings.project_name
    )
  group by project_name
  returning id, name
),
all_projects as (
  select id, name from public.projects
),
inserted_phases as (
  insert into public.phases (project_id, name, order_index, status)
  select
    p.id,
    coalesce(ws.phase_name, 'Giai doan ' || (ws.phase_order + 1)::text),
    ws.phase_order,
    coalesce(ws.phase_status, case when ws.phase_order = 0 then 'DOING' else 'TODO' end)
  from workflow_settings ws
  join all_projects p on p.name = ws.project_name
  where not exists (
    select 1
    from public.phases ph
    where ph.project_id = p.id
      and ph.order_index = ws.phase_order
      and ph.name = coalesce(ws.phase_name, 'Giai doan ' || (ws.phase_order + 1)::text)
  )
  returning id, project_id, name, order_index
),
phase_map as (
  select
    ws.payload,
    ph.id as phase_id
  from workflow_settings ws
  join all_projects p on p.name = ws.project_name
  join public.phases ph
    on ph.project_id = p.id
   and ph.order_index = ws.phase_order
   and ph.name = coalesce(ws.phase_name, 'Giai doan ' || (ws.phase_order + 1)::text)
)
insert into public.tasks (phase_id, name, assignee, deadline, note, status)
select
  pm.phase_id,
  task_item->>'name',
  task_item->>'assignee',
  task_item->>'deadline',
  task_item->>'note',
  coalesce(task_item->>'status', 'TODO')
from phase_map pm
cross join lateral jsonb_array_elements(coalesce(pm.payload->'tasks_list', '[]'::jsonb)) task_item
where coalesce(task_item->>'name', '') <> ''
  and not exists (
    select 1
    from public.tasks t
    where t.phase_id = pm.phase_id
      and t.name = task_item->>'name'
  );

